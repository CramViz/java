    private void btnValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValiderActionPerformed
        // TODO add your handling code here:
        if(txtNomArticle.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Saisir un nom d'article","Erreur de saisie",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Article Larticle=new Article(txtNomArticle.getText(), spNbFeuillets.getValue());
            
            if (!mesEditions.containsKey(cboMagazines.getSelectedItem().toString())){
             
                ArrayList<Article> lesArticles = new ArrayList<>();
              lesArticles.add(Larticle);
              HashMap<String, ArrayList<Article>> lesMagazine = new HashMap<>();
              lesMagazine.put(cboPigistes.getSelectedItem().toString(), lesArticles);
              mesEditions.put(cboMagazines.getSelectedItem().toString(), lesMagazine);
            }
             else if (!mesEditions.get(cboMagazines.getSelectedItem().toString()).containsKey(cboPigistes.getSelectedItem().toString())){
                ArrayList<Article> lesArticles = new ArrayList<>();
              lesArticles.add(Larticle);
               mesEditions.get(cboMagazines.getSelectedItem().toString()).put(cboPigistes.getSelectedItem().toString(), lesArticles);
               
            }
             else{
                 mesEditions.get(cboMagazines.getSelectedItem().toString()).get(cboPigistes.getSelectedItem().toString()).add(Larticle);
             }
            Remplir();
            
            
        }
        // A vous de jouer
        
    }//GEN-LAST:event_btnValiderActionPerformed

