    public void Remplir() {
        racine.removeAllChildren();
        for (String edition : mesEditions.keySet())
        {
            noeudEditions = new DefaultMutableTreeNode(edition);
            for (String pigiste : mesEditions.get(edition).keySet())
            {
                noeudPigiste = new DefaultMutableTreeNode(pigiste);
                for (Article larticle : mesEditions.get(edition).get(pigiste))
                {
                    noeudArticle = new DefaultMutableTreeNode(larticle.getNomArticle());
                    noeudPigiste.add(noeudArticle);
                    noeudArticle = new DefaultMutableTreeNode(larticle.getsp());
                    noeudPigiste.add(noeudArticle);
                }
                noeudEditions.add(noeudPigiste);
            }
            racine.add(noeudEditions);
        }
        model = new DefaultTreeModel(racine);
        trEditions.setModel(model);
        for(int i = 0 ; i < trEditions.getRowCount() ; i++)
        {
            trEditions.expandRow(i);
            
           
        }
       
    }
